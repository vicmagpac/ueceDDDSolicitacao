package modelo;

public class Funcionario {
	private int id;
	private String nome;
	private boolean chefe;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public boolean isChefe() {
		return chefe;
	}
	public void setChefe(boolean chefe) {
		this.chefe = chefe;
	}
	
	
	
}
